package day8;

public class typecasting {

	public static void main(String[] args) {
		
		
		// implicitCasting
		
		int num = 10;
		long num1 = num;
		System.out.println(num1);
		
		
		//explicit
		
		long value = 3214;
		int value1 = (int) value;
		System.out.println(value1);
	}

}
